<h2>How To Get To The Terminal</h2>
<hr>
<p>The first step to using any Linux system is to open up the terminal.
 The Terminal us used for many different processes and there are many useful commands in this guide that you will learn about to aid you.
 On Linux distributions and MAC OS you can simply search for terminal using the search function and it will open.
 You can also use Ctrl+Alt+T to open terminal if you are using Ubuntu or Mint, or right-click and choose 'Open in terminal'.
</p>

